    public void limpaCampo() {
        jTextData.setText(null);
        jTextMedico.setText(null);
        jTextPaciente.setText(null);
        if (model != null) {
            model.limpaTabela();
        }
    }

    public BufferedImage getBufferedImage(byte[] data) {
        InputStream in = new ByteArrayInputStream(data);
        BufferedImage bImageFromConvert = null;
        try {
            bImageFromConvert = ImageIO.read(in);
        } catch (IOException ex) {
            Logger.getLogger(CadastroPacienteNew.class.getName()).log(Level.SEVERE, null, ex);
        }
        return bImageFromConvert;
    }

    public void getArquivo(int codExame, int codPaciente, int codMedico, String data, byte[] imagem) {
        limpaCampo();
        BufferedImage image = getBufferedImage(imagem);
        //Image imagem = image.getScaledInstance(136, 148, 0);
        Icon icon = new ImageIcon(image);
        jLabelImagem.setIcon(icon);
        if (codExame == 0) {
            jTextData.setText("");
            jTextPaciente.setText("");
            jTextMedico.setText("");
            jTextExame.setText("");
        } else {
            jTextData.setText(data);
            jTextPaciente.setText(codPaciente+"");
            jTextMedico.setText(codMedico+"");
            jTextExame.setText(codExame+"");
        }
    }

